int pickingNumbers(vector<int> a) {

    int max=0;
    int n=a.size();

    sort(a.begin(),a.end());

    for(int i=0;i<n;i++)
    {
        int count=1;
        for(int j=i+1;j<n;j++)
        {
            if(a[j]>a[i]+1)
            {
                break;
            }
            else
            {
                count++;
            }
        }

        if(count>max)
        {
            max=count;
        }
    }

    return max;

}

